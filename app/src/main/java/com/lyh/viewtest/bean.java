package com.lyh.viewtest;

public class bean {
    String name;
    String age;
    String num;

    public bean(String name, String age, String num) {
        this.name = name;
        this.age = age;
        this.num = num;
    }

    public String getName() {
        return name;
    }

    public String getAge() {
        return age;
    }

    public String getNum() {
        return num;
    }
}
